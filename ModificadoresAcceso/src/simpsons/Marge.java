package simpsons;

public class Marge {

	private double largoDePelo;
	private int edad;
	private String hobby;

	protected double getLargoDePelo() {
		return largoDePelo;
	}
	protected void setLargoDePelo(double largoDePelo) {
		this.largoDePelo = largoDePelo;
	}
	protected int getEdad() {
		return edad;
	}
	protected void setEdad(int edad) {
		this.edad = edad;
	}
	protected String getHobby() {
		return hobby;
	}
	protected void setHobby(String hobby) {
		this.hobby = hobby;
	}
		
}